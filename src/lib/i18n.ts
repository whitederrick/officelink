// Dict 생성기
function build(lang: Lang): Dict {
  const out: Dict = {};
  for (const k in BASE) {
    out[k] = BASE[k][lang] || BASE[k].ko;
  }
  return out;
}

export const DICT: Record<Lang, Dict> = {
  ko: build("ko"),
  en: build("en"),
  ja: build("ja"),
  zh: build("zh"),
};

const LANG_KEY = "officelink:lang";

export function getLang(): Lang {
  if (typeof window === "undefined") return "ko";
  const v = window.localStorage.getItem(LANG_KEY);
  if (v === "en" || v === "ja" || v === "zh") return v;
  // 자동 감지
  if (!v) {
    const browser = window.navigator?.language || "ko";
    if (browser.startsWith("en")) return "en";
    if (browser.startsWith("ja")) return "ja";
    if (browser.startsWith("zh")) return "zh";
  }
  return "ko";
}

export function setLang(lang: Lang) {
  if (typeof window === "undefined") return;
  window.localStorage.setItem(LANG_KEY, lang);
  if (typeof document !== "undefined") {
    document.documentElement.lang = lang;
  }
  window.dispatchEvent(new CustomEvent("officelink:lang", { detail: lang }));
}

export function t(key: string, lang: Lang = "ko"): string {
  return DICT[lang]?.[key] || DICT.ko[key] || key;
}

export function getDict(lang: Lang): Dict {
  return DICT[lang] ?? DICT.ko;
}

